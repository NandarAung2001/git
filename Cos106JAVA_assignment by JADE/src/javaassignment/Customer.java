    private void T1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_T1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel m  = new DefaultTableModel();
        m = (DefaultTableModel)T1.getModel();
        int i = T1.getSelectedRow();
        txtId.setText(m.getValueAt(i, 0).toString());
        txtName.setText(m.getValueAt(i, 1).toString());
        txtAddress.setText(m.getValueAt(i, 2).toString());
        txtPhone.setText(m.getValueAt(i, 3).toString());
        txtEmail.setText(m.getValueAt(i, 4).toString());
        String gender = m.getValueAt(i, 5).toString();
        if (gender.equals("Male"))
        {
            rdb2.setSelected(false);
            rdb1.setSelected(true);
        }
        else if (gender.equals("Female"))
        {
            rdb1.setSelected(false);
            rdb2.setSelected(true);
        }
        txtAge.setText(m.getValueAt(i, 6).toString());
    }//GEN-LAST:event_T1MouseClicked

