    public void Display()
    {
        ResultSet rs = null;
        Statement stmt = null;
        Connection con = null;
        DefaultTableModel m = new DefaultTableModel();
        Object[] row = new Object[7];
        String url = "jdbc:mysql://localhost:3306/RepairDB";
        String driver = "com.mysql.cj.jdbc.Driver";
        String username = "root";
        String password = "root";
        m = (DefaultTableModel)T1.getModel();
        m.setRowCount(0);
        try 
        {
            con = DriverManager.getConnection(url,username,password);
            stmt = con.createStatement();
            String sql = "select * from customer";
            rs = stmt.executeQuery(sql);
            while (rs.next())
            {
                row[0] = rs.getString("customer_id");
                row[1] = rs.getString("customer_name");
                row[2] = rs.getString("customer_address");
                row[3] = rs.getString("customer_tel");
                row[4] = rs.getString("customer_email");
                row[5] = rs.getString("customer_gender");
                row[6] = rs.getString("customer_age");
                m.addRow(row);
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }

